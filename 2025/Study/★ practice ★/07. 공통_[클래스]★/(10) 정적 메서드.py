# 덧셈과 곱셈을 하는 클래스
class Calc:
    @staticmethod
    def add(a,b):
        print(a + b)

    @staticmethod
    def mul(a,b):
        print(a*b)

Calc.add(10,20)  #인스턴스를 넣지 않고,  클래스에서 바로 메서드 호출
Calc.mul(10,20)
