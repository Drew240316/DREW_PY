# 문자열의 왼쪽 정렬하기
# ljust(길이)는 문자열을 지정된 길이로 만든 뒤 왼쪽으로 정렬하며 남는 공간은 공배긍로 채운다.
'python'.ljust(10)



# 문자열을 오른쪽으로 정렬하기
# rjust(길이)

'python'.rjust(10)



# 문자열을 가운데 정렬하기
# center(길이)
'python'.center(10)



c = 'python'.ljust(10)
print(c)

d = 'python'.rjust(10)
print(d)


e = 'python'.center(10)
print(e)
